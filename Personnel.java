public class Personnel {
    String personnelID;
    String email;
    int ContactNumber;
    String name;

    //Personnel will be Loaded in by Driver
    Personnel(String personnelID, String email, int contactNumber, String name){
        this.personnelID=personnelID;
        this.email=email;
        ContactNumber=contactNumber;
        this.name=name;
    }
}
