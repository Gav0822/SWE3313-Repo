public class Driver {
    String City;
    String State;
    String Street;
    int Zipcode;
    String Name;
    String DriverID;
    int ContactNumber;
    String truckType;

    Driver(String city, String state, String street, int zipcode, String name, String driverID, int contactNumber, String trucktype){
        City=city;
        State=state;
        Street=street;
        Zipcode=zipcode;
        Name=name;
        DriverID=driverID;
        ContactNumber=contactNumber;
        truckType=trucktype;

    }
}
